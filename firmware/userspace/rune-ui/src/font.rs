//! Tiny 5x7 bitmap font.

use crate::canvas::{Canvas, Color};

/// Draws one character.
pub fn draw_char(canvas: &mut Canvas, x: i32, y: i32, ch: char, scale: i32, color: Color) {
    let glyph = glyph(ch);
    for (row, bits) in glyph.iter().enumerate() {
        let bits = *bits;
        for col in 0..5 {
            if bits & (1 << (4 - col)) != 0 {
                canvas.fill_rect(
                    crate::canvas::Rect::new(
                        x + col * scale,
                        y + row as i32 * scale,
                        scale,
                        scale,
                    ),
                    color,
                );
            }
        }
    }
}

